num1 = 10
num2 = 5

# Arithmetic Operators
print("num1 + num2 is ", num1 + num2)
print("num1 - num2 is ", num1 - num2)
print("num1 * num2 is ", num1 * num2)
print("num1 / num2 is ", num1 / num2)
print("num1 // num2 is ", num1 // num2)
print("num1 % num2 is ", num1 % num2)
print("num1 ** num2 is ", num1 ** num2)


# Assignment Operators
a = 4
a += 2
a -= 3
a *= 5


# Comparison Operators
x = 8
y = 6
z = 8
print(x > y)
print(x < y)
print(x != y)
print(x == z)


# Logical Operators
print(x == z and x < y)
print(x == z or x < y)
print(not(False))
print(not(True))